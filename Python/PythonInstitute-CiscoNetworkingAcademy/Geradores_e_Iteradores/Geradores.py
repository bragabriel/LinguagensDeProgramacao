# Um gerador Python é uma peça de código especializado capaz de produzir uma série de valores, e de controlar o processo de iteração.
# Por isso que os geradores são muitas vezes chamados iteradores

# Um exemplo básico:
for i in range(5):
    print(i)

# A função range() é, de facto, um gerador, que é (de facto, mais uma vez) um iterador.