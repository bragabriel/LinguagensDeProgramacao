# Função ORD():
print("\nfunção ord()")
# Se quiser saber o valor do code point ASCII/UNICODE 
#  de um caratere específico, pode usar uma função
#  chamada ord() (como em ordinal).


# Demonstrating the ord() function.
char_1 = 'a'
char_2 = ' '  # space

print(ord(char_1))
print(ord(char_2))



# Função CHR():
print("\nfunção chr()")
# Demonstrating the chr() function.
print(chr(97))
print(chr(945))

