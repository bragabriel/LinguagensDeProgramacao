# str()
# A conversão número-string é simples, pois é sempre possível. É feita por uma função chamada str().

itg = 13
flt = 1.3
si = str(itg)
sf = str(flt)

print(si + ' ' + sf)


# int()
# Utilize a função int() , se quiser obter um número inteiro, e float() se precisar de um valor de floating-point.

si = '13'
sf = '1.3'
itg = int(si)
flt = float(sf)

print(itg + flt)
